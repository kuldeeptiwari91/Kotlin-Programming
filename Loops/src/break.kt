fun main() {
    var number = 0

    // 'while' loop checks the condition before each iteration.
    while (number < 10) {
        number++

        // If the number equals 7, the loop will break.
        if (number == 7) {
            // 'break' is used to exit the loop when the condition is satisfied.
            break
        }

        // Print the current value of number.
        println(number)
    }
}
