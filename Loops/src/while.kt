fun main() {
    // 'while' is another way to create loops in Kotlin.
    // The 'while' loop checks the condition before executing the loop's body.
    // If the condition is true, it executes the loop's body; if false, it exits the loop.

    var number = 0

    // This 'while' loop will print numbers from 0 to 9.
    while (number < 10) {
        println(number)
        number++
        // The number variable is incremented by 1 in each iteration.
        // The loop continues until 'number' is no longer less than 10.
    }
}
